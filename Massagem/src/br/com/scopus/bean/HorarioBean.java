package br.com.scopus.bean;

public class HorarioBean
{
	int idHorario;
	String turnoHorario;
	String descricaoHorario;

	public int getIdHorario()
	{
		return idHorario;
	}

	public void setIdHorario(int idHorario)
	{
		this.idHorario = idHorario;
	}

	public String getTurnoHorario()
	{
		return turnoHorario;
	}

	public void setTurnoHorario(String turnoHorario)
	{
		this.turnoHorario = turnoHorario;
	}

	public String getDescricaoHorario()
	{
		return descricaoHorario;
	}

	public void setDescricaoHorario(String descricaoHorario)
	{
		this.descricaoHorario = descricaoHorario;
	}	
}